def exponent(base, exp):
    # Calculate base raised to the power of exp using the ** operator
    return base ** exp

# Example usage
base1 = 2
exp1 = 5
result1 = exponent(base1, exp1)
print(f"{base1} raises to the power of {exp1}: {result1}")

base2 = 5
exp2 = 4
result2 = exponent(base2, exp2)
print(f"{base2} raises to the power of {exp2}: {result2}")
